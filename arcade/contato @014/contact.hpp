#pragma once
#include <iostream>
#include <list>
#include "fone.hpp"

class Contact {
private:
    std::list<Fone> fones;
    std::string name{""};
public:
    ///////////////////////////////////////////////////////////////////////////
    ///////////////Constructor/////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    Contact(std::string name = "") :
        name{name} {
    }

    ///////////////////////////////////////////////////////////////////////////
    ///////////////Methods/////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    bool addFone(Fone fone) {
        if (!fone.isValid()) {
            return false;
        }
        this->fones.push_back(fone);
        return true;
    }

    ///////////////////////////////////////////////////////////////////////////
    ///////////////Get and Set Methods/////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////

    std::list<Fone> getFones() {
        return this->fones;
    }

    std::string getName() {
        return this->name;
    }

    // Sets the number only if it's a valid number
    bool setFones(Fone fone) {
        if (!addFone(fone)) {
            return false;
        }
        return true;
    }

    void setName(std::string name) {
        this->name = name;
    }
};